package com.uwsoft.editor.data;

public class LevelShortData {

	private int id;
	
	private int number;
	
	private String name;
	
	private int episode_id;
	
	public String getName() {
		return name;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getNumber() {
		return number;
	}

	public void setNumber(int number) {
		this.number = number;
	}
	
	public int getEpisodeId() {
		return episode_id;
	}
}
